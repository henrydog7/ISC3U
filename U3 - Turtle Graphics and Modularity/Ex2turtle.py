import turtle

#set up the turtle features
turtle.bgcolor("light green")
turtle.title ("Turtle Commands Example 2")
turtle.shape("arrow")
turtle.speed(1)

#make the turtle draw a C
turtle.hideturtle()
turtle.backward(100)
turtle.setheading(90)
turtle.forward(50)
turtle.setheading(0)
turtle.forward(100)

turtle.hideturtle()

